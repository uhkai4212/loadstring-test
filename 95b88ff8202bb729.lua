    infobutton.Image = "http://www.roblox.com/asset/?id=6294110112"
    infobutton.Name = "infobutton"
    infobutton.Parent = button

    local slider = Instance.new("Frame")
    slider.Position = UDim2.new(0, 345, 0, 15)
    slider.Size = UDim2.new(0, 25, 0, 10)
    slider.BackgroundColor3 = Color3.fromRGB(200, 0, 0)
    slider.BorderSizePixel = 0
    slider.Name = "slider"
    slider.Parent = button

    local sliderCorner = Instance.new("UICorner")
    sliderCorner.CornerRadius = UDim.new(0, 5)
    sliderCorner.Name = "UICorner"
    sliderCorner.Parent = slider

    local frame = Instance.new("Frame")
    frame.Position = UDim2.new(0, -5, 0, -3)
    frame.Size = UDim2.new(0, 15, 0, 15)
    frame.Name = "Frame"
    frame.Parent = slider

    local frameCorner = Instance.new("UICorner")
    frameCorner.CornerRadius = UDim.new(0, 20)
    frameCorner.Name = "UICorner"
    frameCorner.Parent = frame

    -- Store button reference for search functionality
    table.insert(buttons, button)

    -- Button click functionality
    button.MouseButton1Click:Connect(function()
        local humanoid = game.Players.LocalPlayer.Character:FindFirstChildOfClass("Humanoid")
        if humanoid then
            local animationIdsList = type(animationIds) == "table" and animationIds or {animationIds} -- Ensure animationIds is a table

            -- Flag to check if any animations are currently active
            local isActive = false 

            -- Iterate through each animation ID
            for _, animationId in ipairs(animationIdsList) do
                if activeAnimations[animationId] then
                    -- If the animation is already active, stop it
                    activeAnimations[animationId]:Stop()
                    activeAnimations[animationId] = nil -- Remove from active animations table
                else
                    -- Load and play the new animation
                    local anim = Instance.new("Animation")
                    anim.AnimationId = "rbxassetid://" .. animationId
                    local animationTrack = humanoid:LoadAnimation(anim)
                    animationTrack.Looped = true -- Enable looping
                    animationTrack:Play()
                    
                    -- Store the active animation track
                    activeAnimations[animationId] = animationTrack
                    isActive = true -- Mark that at least one animation is active
                end
            end
            
            -- Change the button color based on active state
            local tweenService = game:GetService("TweenService")
            if isActive then
                local colorTween = tweenService:Create(slider, TweenInfo.new(0.5), {BackgroundColor3 = Color3.fromRGB(0, 255, 0)})
                local frameTween = tweenService:Create(frame, TweenInfo.new(0.5), {Position = UDim2.new(1, -10, 0, -3)})
                colorTween:Play()
                frameTween:Play()
            else
                local colorTween = tweenService:Create(slider, TweenInfo.new(0.5), {BackgroundColor3 = Color3.fromRGB(200, 0, 0)})
                local frameTween = tweenService:Create(frame, TweenInfo.new(0.5), {Position = UDim2.new(0, -5, 0, -3)})
                colorTween:Play()
                frameTween:Play()
            end
            
            -- Adjust speed from input box for each active animation
            for _, animationId in ipairs(animationIdsList) do
                if activeAnimations[animationId] then
                    activeAnimations[animationId]:AdjustSpeed(tonumber(SpeedInput.Text) or 1)
                end
            end
        end
    end)
end

