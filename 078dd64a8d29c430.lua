    -- Generated by @TheMugenKing
local hrp = game:GetService('Players').LocalPlayer.Character:WaitForChild('HumanoidRootPart')
local SpawnList = {
    Vector3.new(743.17, 30.71, 130.41),
}
for _, pos in ipairs(SpawnList) do task.wait(0.4) hrp.CFrame = CFrame.new(pos) end
end)

