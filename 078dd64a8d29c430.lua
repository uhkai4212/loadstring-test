local Library = loadstring(game:HttpGet("https://raw.githubusercontent.com/TheMugenKingMods/Gui-Library/refs/heads/main/Gerad's"))()

local Window = Library:CreateWindow('ArceusXArchive') -- :CreateWindow(Title)

local Section = Window:Section('Feature') -- :Section(Title)

Section:Button('Lobby Win', function() -- :Button(Title, callback)
    local Players = game:GetService("Players")
local player = Players.LocalPlayer
local hrp = player.Character and player.Character:WaitForChild("HumanoidRootPart")

for i = 1, 5 do
    local winPart = workspace:FindFirstChild("Win" .. i)
    if winPart then
        local p = (winPart:IsA("BasePart") and winPart) or winPart:FindFirstChildWhichIsA("BasePart", true)
        if p then
            hrp.CFrame = p.CFrame
        end
    end
end
end)

